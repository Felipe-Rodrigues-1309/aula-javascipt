let cachorro = { // Define um objeto chamado "cachorro"
    patas: 4, // Propriedade "patas" com valor 4
    nome: 'betavem', // Propriedade "nome" com valor 'betavem'
    idade: 5, // Propriedade "idade" com valor 5
    latir: function(){ // Método "latir" que exibe "Au Au" no console
        console.log("Au Au"); // Exibe "Au Au" no console
    }
};

console.log(cachorro.patas); // Exibe o valor da propriedade "patas" do objeto "cachorro"
console.log(cachorro.nome); // Exibe o valor da propriedade "nome" do objeto "cachorro"
cachorro.latir(); // Chama o método "latir" do objeto "cachorro"



