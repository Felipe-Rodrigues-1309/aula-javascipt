const felipe = (x) => {
     return x + 2; }; // Função arrow que retorna o valor de 'x' somado a 2.
console.log(felipe(99)); // Chama a função 'felipe' com 99 e exibe o resultado (101) no console.

const felipe2 = (x) => x * 9; // Função arrow que retorna o valor de 'x' multiplicado por 9.
console.log(felipe2(8)); // Chama a função 'felipe2' com 8 e exibe o resultado (72) no console.